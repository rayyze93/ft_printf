#include "ft_printf.h"

void	ft_putnbr(int n, int *i)
{
	unsigned int	nb;

	if (n < 0)
	{
		ft_putchar('-', i);
		nb = n * -1;
	}
	else
		nb = n;
	if (nb > 9)
		ft_putnbr((nb / 10), i);
	ft_putchar('0' + (nb % 10), i);
}
