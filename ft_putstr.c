#include "ft_printf.h"

void	ft_putstr(char *s, int *i)
{
	int	j;

	if (!s)
		ft_putstr("(null)", i);
	else
	{
		j = 0;
		while (s[j])
			ft_putchar(s[j++], i);
	}
}
